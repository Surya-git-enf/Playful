def simulate_gemini(prompt: str) -> Dict[str,Any]:
    return {
        "project_name": "mount_climb_demo",
        "files": [
            {"path":"index.html","type":"text","content":"<!doctype html><html><head><meta charset='utf-8'><meta name='viewport' content='width=device-width,initial-scale=1'></head><body><h1>Mount Climb Demo</h1><script src='js/app.js'></script></body></html>"},
            {"path":"js/app.js","type":"text","content":"console.log('MountClimb demo');"},
            {"path":"css/style.css","type":"text","content":"body{font-family:Arial;background:#111;color:#fff}"},
        ],
        "estimated_credits": 1,
        "notes": "simulated manifest"
    }

def call_gemini(prompt: str) -> Dict[str,Any]:
    if BUILD_MODE == "simulate":
        return simulate_gemini(prompt)
    if not GEMINI_API_ENDPOINT or not GEMINI_API_KEY:
        raise RuntimeError("Gemini endpoint/key not configured")
    headers = {"Authorization": f"Bearer {GEMINI_API_KEY}", "Content-Type": "application/json"}
    payload = {"prompt": prompt, "max_tokens": 4000, "temperature": 0.2}
    r = requests.post(GEMINI_API_ENDPOINT, headers=headers, json=payload, timeout=120)
    if r.status_code >= 400:
        raise RuntimeError(f"AI call failed: {r.status_code} {r.text}")
    data = r.json()
    txt = None
    if isinstance(data, dict):
        if "candidates" in data and isinstance(data["candidates"], list):
            txt = data["candidates"][0].get("content") or data["candidates"][0].get("text")
        elif "output" in data:
            txt = data["output"]
        else:
            txt = json.dumps(data)
    else:
        txt = str(data)
    first = txt.find("{")
    last = txt.rfind("}")
    if first == -1 or last == -1:
        raise RuntimeError("AI did not return JSON manifest")
    manifest = json.loads(txt[first:last+1])
    return manifest

# -------------------------
# sanitize files
# -------------------------
def sanitize_files(files: List[Dict[str,Any]]) -> List[Dict[str,Any]]:
    allowed = []
    for f in files:
        path = f.get("path","")
        typ = f.get("type","text")
        content = f.get("content","")
        if typ == "text":
            content = re.sub(r'\beval\s*\(', '/*eval*/(', content)
            content = re.sub(r'new\s+Function\s*\(', '/*new Function*/(', content)
            allowed.append({"path":path, "type":"text", "content":content})
        elif typ == "binary_base64":
            size_bytes = (len(content) * 3)//4
            if size_bytes > 50*1024*1024:
                raise RuntimeError(f"Binary {path} too large")
            allowed.append({"path":path, "type":"binary_base64", "content":content})
        else:
            raise RuntimeError("Unknown file type")
    return allowed

